// app.js —— 小程序全局入口文件，整个小程序只有一个 App 实例
App({
  // 1. 小程序生命周期钩子：初始化完成时触发（全局只触发一次）
  onLaunch() {
    // 小程序冷启动/热启动时都会执行，适合做全局初始化操作
    console.log('小程序启动了');
    
    // 常用初始化场景：
    // 1. 获取用户权限
    // 2. 检查更新
    // 3. 请求全局接口（如获取配置、token）
    // 4. 本地存储数据读取
  },

  // 2. 全局数据对象：所有页面都可以访问和修改这里的数据
  globalData: {
    userInfo: null,  // 存储用户信息（全局共享）
    // 可以继续添加全局数据：
    token: '',       // 用户登录凭证
    systemInfo: {}   // 设备系统信息
  },

  // 3. 可选：小程序切前台时触发
  onShow() {
    console.log('小程序切回前台了');
  },

  // 4. 可选：小程序切后台时触发
  onHide() {
    console.log('小程序切到后台了');
  },

  // 5. 可选：小程序发生脚本错误时触发
  onError(err) {
    console.log('小程序报错：', err);
  }
})